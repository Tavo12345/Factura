private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
    int cantEnt = 0;
    int costEnt = 0;
    int cantAct = 0;
    int costAct = 0;        
    if(FechaValida() && ItemValido()){
        control.ConeccionEntrada conetEntrada = new control.ConeccionEntrada();
        model.Entrada entrada = new model.Entrada(0, Integer.parseInt(codItem.getText()),
                Integer.parseInt(cantItemEnt.getText()), Integer.parseInt(costUItemEnt.getText()),
                "" + dia.getSelectedItem() + "/" + mes.getSelectedItem() + "/" + año.getSelectedItem());
        control.ConeccionExistencia conetExiste = new control.ConeccionExistencia();
        model.Existencia existe = conetExiste.Consulta(item.getCodItem());
        cantEnt = entrada.getCantItemEnt();
        costEnt = entrada.getCostUItemEnt();
        cantAct = existe.getCantItem();
        costAct = existe.getCostUItem();
        existe.setCantItem(cantAct + cantEnt);
        existe.setCostUItem(((costAct * cantAct) + (costEnt * cantEnt))/(cantAct + cantEnt));
        conetEntrada.Insertar(entrada.getCodItem(), entrada.getCantItemEnt(), entrada.getCostUItemEnt(), entrada.getFechEnt());
        conetExiste.Modificar(existe.getCodItem(), existe.getCantItem(), existe.getCostUItem());
        conetExiste.Close();
        conetEntrada.Close(); 
        IncForm();
    }else{
         JOptionPane.showMessageDialog(null, "Datos incorrectos o el item no es compatible");
    }
}//GEN-LAST:event_jButton1ActionPerformed

