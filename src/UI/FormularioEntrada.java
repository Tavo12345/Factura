    public boolean FechaValida(){
        boolean valida = false;
        if(!(dia.getModel().getSelectedItem().toString().equals("Dia")) &&
           !(mes.getModel().getSelectedItem().toString().equals("Mes")) &&
           !(año.getModel().getSelectedItem().toString().equals("Año"))){            
            valida = true;
        }                
        return valida;
    }
    
    public boolean FormCompleto(){
        boolean valido = false;
        if(!codItem.getText().equals("") && !cantItemEnt.getText().equals("") && !costUItemEnt.getText().equals("")){
            valido = true;
        }
        return valido;
    }
    
    public boolean ItemValido(){
        boolean valido = false;                
        control.ConeccionItem conetItem = new control.ConeccionItem();   
        item = new model.Item(0, null, null, 0);
        item = conetItem.Consulta(Integer.parseInt(codItem.getText()));
        if(FormCompleto()){
            if((item.getCodItem() == Integer.parseInt(codItem.getText())) && item.getTipoItem().equals("Producto")){
                valido = true;
            }
        }
        conetItem.Close();
        return valido;
    }
    
    public void IncForm(){
        dia.setSelectedIndex(0);
        mes.setSelectedIndex(0);
        año.setSelectedIndex(0);
        codItem.setText("");
        cantItemEnt.setText("");
        costUItemEnt.setText("");
    }
    
