    public boolean CodigosFact(){
        boolean validos = false;
        if(!codigoF.getText().equals("") && !codigoC.getText().equals("") && !codigoD.getText().equals("")){
            validos = true;
        }
        return validos;
    }
    
    public boolean DatosItem(){
        boolean validos = false;
        if(!codigoI.getText().equals("") && !cantidad.getText().equals("") && !codigoD.getText().equals("")){
            validos = true;
        }
        return validos;
    }
    
    public boolean NoExistItemD(int codItem){
        boolean valido = true;
        for(int i = 0;i < detalles.size();i++){
            if(detalles.get(i).getCodItem() == codItem){
                valido = false;
            }
        }
        return valido;
    }
    
    public boolean ExistItemP(int codItem, int cant){
        boolean valido = true;
        control.ConeccionExistencia conetExist = new control.ConeccionExistencia();
        existe = conetExist.Consulta(codItem);
        conetExist.Close();
        if(cant <= existe.getCantItem()){
            valido = true;
        }
        return valido;
    }
    
    public boolean EsServicio(int codItem){
        boolean valido = true;
        control.ConeccionItem conetItem = new control.ConeccionItem();
        item = conetItem.Consulta(codItem);
        conetItem.Close();
        if(item.getTipoItem().equals("Servicio")){
            valido = true;
        }
        return valido;
    }

