private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
    if(DatosItem()){
        if(NoExistItemD(Integer.parseInt(codigoI.getText()))){
            if(ExistItemP(Integer.parseInt(codigoI.getText()),Integer.parseInt(cantidad.getText()))){
                control.ConeccionItem conetItem = new control.ConeccionItem();
                item = conetItem.Consulta(Integer.parseInt(codigoI.getText()));
                conetItem.Close();
                detalle = new model.Detalle(0, 0, 0, 0, 0);
                detalle.setCodDetalle(Integer.parseInt(codigoD.getText()));
                detalle.setCodItem(item.getCodItem());
                detalle.setCantItemFact(Integer.parseInt(cantidad.getText()));               
                if(EsServicio(Integer.parseInt(codigoI.getText()))){
                    detalle.setCostUItemFact(1);
                }else{
                    detalle.setCostUItemFact(existe.getCostUItem());
                }               
                detalle.setValorUVentItem(item.getValorUItem());
                detalles.add(detalle);
                totalVent = 0;                
                DefaultTableModel tablaModelo = new DefaultTableModel(null, new Object[]{"Codigo Item","Nombre","Cantidad","Valor Unitario"});
                for(int i = 0;i < detalles.size();i++){
                    tablaModelo.addRow(new Object[]{detalles.get(i).getCodItem(),NombreItem(detalles.get(i).getCodItem()),
                                                    detalles.get(i).getCantItemFact(),detalles.get(i).getValorUVentItem()});
                    totalVent = totalVent + (detalles.get(i).getCantItemFact() * detalles.get(i).getValorUVentItem());                    
                }
                jTable1.setModel(tablaModelo);
                total.setText("" + ((totalVent * 0.16) + totalVent));
                iva.setText("" + (totalVent * 0.16));
            }else{
                JOptionPane.showMessageDialog(null, "El Item no existe");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Este Item ya existe en esta lista");
        }
    }else{
        JOptionPane.showMessageDialog(null, "Datos incorrectos");
    }
}//GEN-LAST:event_jButton1ActionPerformed

