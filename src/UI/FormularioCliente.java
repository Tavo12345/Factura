private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
    if(FormCompleto()){
        control.ConeccionCliente conetCliente = new control.ConeccionCliente();
        model.Cliente cliente = new model.Cliente(0, null, null, 0, null);
        cliente.setCodCliente(Integer.parseInt(codigo.getText()));
        cliente.setNomCliente(nombre.getText());
        cliente.setDirCliente(direccion.getText());
        cliente.setTelCliente(Integer.parseInt(telefono.getText()));
        cliente.setEmailCliente(email.getText());
        conetCliente.Insertar(cliente.getCodCliente(), cliente.getNomCliente(), cliente.getDirCliente(),
                              cliente.getTelCliente(), cliente.getEmailCliente());
        conetCliente.Close();
        Limpiar();
    }else{
        JOptionPane.showMessageDialog(null, "Datos incorrectos");
    }
}//GEN-LAST:event_jButton1ActionPerformed

