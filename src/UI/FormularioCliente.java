private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
// TODO add your handling code here:
    if(!codigo.getText().equals("")){
        control.ConeccionCliente conetCliente = new control.ConeccionCliente();
        model.Cliente cliente = new model.Cliente(0, null, null, 0, null);
        cliente = conetCliente.Consulta(Integer.parseInt(codigo.getText()));
        conetCliente.Close();
        nombre.setText(cliente.getNomCliente());
        direccion.setText(cliente.getDirCliente());
        telefono.setText("" + cliente.getTelCliente());
        email.setText(cliente.getEmailCliente());        
    }else{
        JOptionPane.showMessageDialog(null, "Datos incorrectos");
    }
}//GEN-LAST:event_jButton3ActionPerformed

    public void Limpiar(){
        codigo.setText("");
        nombre.setText("");
        direccion.setText("");
        telefono.setText("");
        email.setText("");
    }
        
    public boolean FormCompleto(){
        boolean valido = false;
        if(!codigo.getText().equals("") && !nombre.getText().equals("") && !direccion.getText().equals("") &&
           !telefono.getText().equals("") && !email.getText().equals("")){
            valido = true;
        }
        return valido;
    }
    
